# 已知缺陷汇总
# ============================================
#
# 1. 【Prompt 缺乏版本管理】
#    - 当前所有 Prompt 以纯字符串存放，没有版本号和变更日志。
#    - 后续可改用 YAML 文件或数据库存储，支持 A/B 测试不同 Prompt。
#
# 2. 【缺乏少样本示例】
#    - QA Prompt 没有 few-shot 示例，LLM 回答风格可能不稳定。
#    - 后续从中精选 1-2 道真题的标准讲解作为 example 放入 Prompt。
#
# 3. 【未考虑上下文窗口限制】
#    - 无 token 计数机制，检索片段 + 历史对话可能超出模型上下文窗口。
#    - 后续应加入 token 估算与截断逻辑。
#
# 4. 【User Prompt 缺乏结构化指令】
#    - 未使用 XML 标签（如 <question>、<context>）包裹不同部分，
#      某些模型（如 Claude）对结构化标签的遵循度更高。
